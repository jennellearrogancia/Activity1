    public double computeSalary(double employeeHours, double employeeRate, int tax){
        double grossPay = employeeRate * employeeHours;
        double Tax = tax * 0.01;
        double taxD = grossPay * Tax;
        double netPay = grossPay - taxD;
        return netPay;
    }
    
    public double computeSalary(double employeeHours, double employeeRate, double tax){
        double grossPay = employeeRate * employeeHours;
        double taxD = grossPay * tax;
        double netPay = grossPay - taxD;
        return netPay;
    }

