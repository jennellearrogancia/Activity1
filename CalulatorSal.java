    private void jcalculateActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        try {
            double rate = Double.parseDouble(jemprate.getText());
            double hours = Double.parseDouble(jhour.getText());
            String taxInput = jtax.getText();
            double netPay;
            
            if (taxInput.contains(".")) {
                // If tax input contains a decimal point, treat it as a decimal
                double tax = Double.parseDouble(taxInput);
                netPay = computeSalary(hours, rate, tax);
            } else {
                // Otherwise, treat it as an integer percentage
                int tax = Integer.parseInt(taxInput);
                netPay = computeSalary(hours, rate, tax);
            }
            
            // Display the result (you might want to show this in a dialog or a label)
            javax.swing.JOptionPane.showMessageDialog(null, "Net Pay: " + netPay);
        } catch (NumberFormatException e) {
            // Handle invalid input
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter valid numbers for rate, hours, and tax.");
        }                                         
    }                                          

